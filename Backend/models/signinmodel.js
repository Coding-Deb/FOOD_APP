const mongoose = require('mongoose')

const SigninSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    cpassword: {
        type: String,
        required: true,
    },
    

})

module.exports = mongoose.model('signinUser',SigninSchema)